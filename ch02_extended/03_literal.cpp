// 03_literal.cpp
#include <iostream>
using namespace std;
int main(){
    cout << 10 << endl;
    cout << 10U << endl; // unsigned int
    cout << 10L << endl; // long
    cout << 10LL << endl; // long long
    cout << 10.5F << endl; // float
    cout << 10.5 << endl;
    cout << 10.5L << endl; // double
    cout << 3 + 4 << endl;
    return 0;
}